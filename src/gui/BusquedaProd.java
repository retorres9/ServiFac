    public void busquedaProd(String codigo) {
        ArrayList<Producto> producto = manejadorMaterial.busquedaProducto(codigo);
        int cant = producto.size();
        if (cant == 0) {
            lblNombre.setText("");
            lblPVP.setText("");
            lblPrecioMayor.setText("");
            lblCantidad.setText("");
            JOptionPane.showMessageDialog(this, "Producto no encontrado");
        } else {
            for (int i = 0; i < cant; i++) {
                objProd = producto.get(i);
                String nombre = objProd.getStrNombreProd();
                double precioPublico = objProd.getFltPrecio();
                double precioMayor = objProd.getFltPrecioMayor();
                int cantidad = objProd.getIntCantidad();

                lblNombre.setText(nombre);
                lblPVP.setText(String.valueOf(precioPublico));
                lblPrecioMayor.setText(String.valueOf(precioMayor));
                lblCantidad.setText(String.valueOf(cantidad));
            }
        }
        txtCodigo.setText("");
    }

